    private void select_datos(){
        int fila = this.tbl_docente.getSelectedRow();
        lbl_codigonumber.setText(tbl_docente.getValueAt(fila, 0).toString());
        this.txt_nit.setText(tbl_docente.getValueAt(fila, 1).toString());
        this.txt_nombres.setText(tbl_docente.getValueAt(fila, 2).toString());
        this.txt_apellidos.setText(tbl_docente.getValueAt(fila, 3).toString());
        this.txt_direccion.setText(tbl_docente.getValueAt(fila, 4).toString());
        this.txt_telefono.setText(tbl_docente.getValueAt(fila, 5).toString());
        this.txt_fn.setText(tbl_docente.getValueAt(fila, 6).toString());
        this.txt_salario.setText(tbl_docente.getValueAt(fila, 7).toString());
        this.txt_fil.setText(tbl_docente.getValueAt(fila, 8).toString());
        this.txt_fir.setText(tbl_docente.getValueAt(fila, 9).toString());
        
         // Suponiendo que el ID del puesto est√° en la columna 7

}
