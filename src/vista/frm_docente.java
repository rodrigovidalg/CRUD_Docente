    private void btn_agregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_agregarActionPerformed
        // TODO add your handling code here:
        docente = new Docente(0, txt_nit.getText(),txt_nombres.getText(),txt_apellidos.getText(),txt_direccion.getText(),txt_telefono.getText(),txt_fn.getText(), this.txt_salario.getText(), this.txt_fil.getText(), this.txt_fir.getText());
        docente.agregar();
        tbl_docente.setModel(docente.leer());
    }//GEN-LAST:event_btn_agregarActionPerformed

    private void btn_borrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_borrarActionPerformed
        // TODO add your handling code here:
        docente = new Docente();
        docente.setCodigo(Integer.valueOf(this.lbl_codigonumber.getText()));
        docente.eliminar();
        tbl_docente.setModel(docente.leer());
    }//GEN-LAST:event_btn_borrarActionPerformed

